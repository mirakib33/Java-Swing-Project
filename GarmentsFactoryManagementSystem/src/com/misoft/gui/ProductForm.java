    public void saveProducts(){
        String productId = pId.getText();
        String item = pItem.getText();
        String category = pCategory.getText();
        String description = pDescription.getText();
        
        
        String sql = "insert into products (product_id, item, category, description) values (?, ?, ?, ?)";
        
        try {
            Connection con = DBConnection.getConnection();
            PreparedStatement psmt = con.prepareStatement(sql);
            
            psmt.setString(1, productId);
            psmt.setString(2, item);
            psmt.setString(3, category);
            psmt.setString(4, description);
            psmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Saved Successfully");
 
        } catch (Exception e) {
            System.out.println(e);
        }
    }
    
